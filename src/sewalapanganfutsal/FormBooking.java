    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String namaCustomer = jTextField1.getText();
        String noTelp = jTextField2.getText();
        
        Object selectedItemObject = cmbLapangan.getSelectedItem();
        Date tanggalBooking = jDateChooser1.getDate();


        if (namaCustomer.isEmpty() || noTelp.isEmpty() || selectedItemObject == null || tanggalBooking == null) {
            JOptionPane.showMessageDialog(this, "Semua data wajib diisi!", "Input tidak lengkap", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        ItemLapangan itemTerpilih = (ItemLapangan) selectedItemObject;
        int idLapangan = itemTerpilih.getId();
             
        
        try {
            long idCustomer = dapatkanAtauBuatIdCustomer(namaCustomer, noTelp);
            if (idCustomer == 0) {
                JOptionPane.showMessageDialog(this, "Gagal memproses data customer!", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            int hargaPerjam = getHargaPerjam(idLapangan);
            int durasi = jSpinField1.getValue();
            int totalBiaya = hargaPerjam * durasi;
            jTextField3.setText(String.valueOf(totalBiaya));
            
            SimpleDateFormat sdfTime = new SimpleDateFormat("HH:mm:ss");
            String jamMulaiStr = jComboBox2.getSelectedItem().toString() + ":00:00";
            Calendar cal = Calendar.getInstance();
            cal.setTime(sdfTime.parse(jamMulaiStr));
            cal.add(Calendar.HOUR_OF_DAY, durasi);
            String jamSelesaiStr = sdfTime.format(cal.getTime());
            
            String sqlBooking = "INSERT INTO booking(id_lapangan, id_customer, tanggal_booking, jam_mulai, jam_selesai, total_biaya, status) VALUES (?, ?, ?, ?, ?, ?, ?)";
            Connection conn = DBConnection.getConnection();
            PreparedStatement pstBooking = conn.prepareStatement(sqlBooking, Statement.RETURN_GENERATED_KEYS);
            
            pstBooking.setInt(1, idLapangan);
            pstBooking.setLong(2, idCustomer);
            pstBooking.setDate(3, new java.sql.Date(tanggalBooking.getTime()));
            pstBooking.setString(4, jamMulaiStr);
            pstBooking.setString(5, jamSelesaiStr);
            pstBooking.setInt(6, totalBiaya);
            pstBooking.setString(7, "Booked");
            
            int rowsAffected = pstBooking.executeUpdate();
            
            if(rowsAffected > 0) {
                ResultSet generatedKeys = pstBooking.getGeneratedKeys();
                int idBookingBaru = 0;
                if (generatedKeys.next()){
                    idBookingBaru = generatedKeys.getInt(1);
                }
                
                String teksStruk = buatTeksStruk(
                        idBookingBaru, 
                        namaCustomer, 
                        itemTerpilih.getNama(), 
                        tanggalBooking, 
                        jamMulaiStr, 
                        jamSelesaiStr, 
                        totalBiaya 
                );
                JTextArea textArea = new JTextArea(teksStruk);
                textArea.setEditable(false);
                textArea.setFont(new Font("Monospaced", Font.PLAIN, 12));
                JScrollPane scrollPane = new JScrollPane(textArea);
                JOptionPane.showMessageDialog(this, scrollPane, "Booking Berhasil", JOptionPane.INFORMATION_MESSAGE);
                kosongkanForm();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Terjadi error saat proses Booking" + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

