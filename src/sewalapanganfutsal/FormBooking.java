        private int dapatkanIdLapangan(String namaLapangan) {
        String sql = "SELECT id_lapangan FROM lapangan WHERE nama_lengkap = ?";
        try (Connection conn = DBConnection.getConnection()) {
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, namaLapangan);
            try (ResultSet rs = pst.executeQuery()) {
                if(rs.next()) {
                    return rs.getInt("id_lapangan");
                }
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
        return 0;
    }

    private int getHargaPerjam(int idLapangan) throws SQLException {
        String sql = "SELECT harga FROM lapangan WHERE id_lapangan = ?";
        try (Connection conn = DBConnection.getConnection()) {
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setInt(1, idLapangan);
            try (ResultSet rs = pst.executeQuery()) {
                if(rs.next()) {
                    return rs.getInt("harga");
                }
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
        return 0;
    }
    
        private long dapatkanAtauBuatIdCustomer(String nama, String telp) throws SQLException {
        long id = 0;
        String cekSql = "SELECT id_customer FROM customer WHERE no_telepon = ?";
        try (Connection conn = DBConnection.getConnection();
             PreparedStatement pstCek = conn.prepareStatement(cekSql)) {
            
            pstCek.setString(1, telp);
            try (ResultSet rs = pstCek.executeQuery()) {
                if (rs.next()) {
                    id = rs.getLong("id_customer");
                } else {
                    String insertSql = "INSERT INTO customer (nama_customer, no_telepon) VALUES (?, ?)";
                    try (PreparedStatement pstInsert = conn.prepareStatement(insertSql, Statement.RETURN_GENERATED_KEYS)) {
                        pstInsert.setString(1, nama);
                        pstInsert.setString(2, telp);
                        pstInsert.executeUpdate();
                        try (ResultSet generatedKeys = pstInsert.getGeneratedKeys()){
                            if (generatedKeys.next()) {
                                id = generatedKeys.getLong(1);
                            }
                        }
                    }
                }
            }
        }
        return id;
    }
    
    private String buatTeksStruk(int idBooking, String namaCustomer, String namaLapangan, Date tanggal, String jamMulai, String jamSelesai, int totalBiaya){
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            String tanggalStr = sdf.format(tanggal);
            
            Locale localeID = new Locale("in", "ID");
            NumberFormat formatRupiah = NumberFormat.getCurrencyInstance(localeID);
            String hargaStr = formatRupiah.format(totalBiaya).replace(",00", "");
            String waktuMulaiLengkap = tanggalStr + " " + jamMulai;
            String waktuSelesaiLengkap = tanggalStr + " " + jamSelesai;
            
            StringBuilder sb = new StringBuilder();
            sb.append(" === Struk Booking Futsal ===\n\n");
            sb.append("ID Transaksi : ").append(idBooking).append("\n");
            sb.append("Nama Customer : ").append(namaCustomer).append("\n");
            sb.append("Nama Lapangan : ").append(namaLapangan).append("\n");
            sb.append("Waktu Mulai : ").append(waktuMulaiLengkap).append("\n");
            sb.append("Waktu Selesai : ").append(waktuSelesaiLengkap).append("\n");
            sb.append("Total Harga : ").append(hargaStr).append("\n\n");
            sb.append("==============================\n");
            sb.append("     Terima kasih!\n");
            
            return sb.toString();
            
    }
    
